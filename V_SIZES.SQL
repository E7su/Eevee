CREATE OR REPLACE VIEW JIRA_READER.V_SIZES AS
  SELECT
    t.ID    ID,
    t.VALUE VALUE,
    p.SCORE SCORE
  FROM (
         SELECT
           o1.ID          ID,
           o1.CUSTOMVALUE VALUE
         FROM jira.CUSTOMFIELDOPTION o1
         WHERE o1.CUSTOMFIELD = 12484
       ) t
    JOIN
    (
      SELECT NULL VALUE, NULL SCORE FROM DUAL WHERE 1=0
      UNION ALL SELECT 'L', 16 FROM dual
      UNION ALL SELECT 'M',  8 FROM dual
      UNION ALL SELECT 'S',  4 FROM dual
      UNION ALL SELECT 'XS', 2 FROM dual
      UNION ALL SELECT 'XXS',1 FROM dual
    ) p ON t.VALUE = p.VALUE;

COMMENT ON COLUMN V_SIZES.ID IS 'ID';
COMMENT ON COLUMN V_SIZES.VALUE IS 'Значение';
COMMENT ON COLUMN V_SIZES.SCORE IS 'Вес';
